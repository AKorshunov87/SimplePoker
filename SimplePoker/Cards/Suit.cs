namespace SimplePoker {
    /// <summary>
    /// In standard poker there is no ranking of suits. Standard poker rules do, however, specify a hierarchy of suits: spades (highest), hearts, diamonds, clubs (lowest)
    /// </summary>
    public enum Suit {
        Clubs = 1,
        Diamonds = 2,
        Hearts = 3,
        Spades = 4
    }
}
